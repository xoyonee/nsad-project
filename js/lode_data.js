function get_url_info(key) {
    let url = location.href; 

    url = url.split('?') 
    
    if(url.length > 1) {
        url = url[1];
        url = url.split("#");
        url = url[0].split("&");
        for(let i=0; i< url.length; i++) {
            let tmp_url = url[i].split("=");
            if(key == tmp_url[0]) {
                return tmp_url[1];
            }
        }
        return null;
    }
    else {
        return null
    }
};